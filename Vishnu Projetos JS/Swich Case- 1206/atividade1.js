/*
Escreva uma função que retorne a área de um triângulo, a partir dos valores de base e altura fornecidos
*/

function areatriangulo(base, altura){ // declarar os argumentos dentro do parenteses
return base * altura / 2 // volta base vezes altura dividido por dois
}
console.log (areatriangulo(5,10))// declarar dentro dos parenteses os valores dos argumentos